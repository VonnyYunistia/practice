package Class8;

public class E1Whileloop {
    public static void main(String[] args) {

        //to print all whoe numbers from 15-30

        int start=15;
        while(start<=30){

            System.out.print(" "+start);

            start++;
        }

        System.out.println();

        for (int i = 15; i <31 ; i++) {

            System.out.print(" "+i);

        }














    }
}
