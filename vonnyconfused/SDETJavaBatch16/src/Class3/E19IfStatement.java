package Class3;

public class E19IfStatement {

    public static void main(String[] args) {

        if(true)

        {
            System.out.println("Time to study"); // this part will execute if part because the statement if is true


        }

        else {
            System.out.println("Time to work");
        }

        if(false)

        {
            System.out.println("Time to study");


        }

        else {
            System.out.println("Time to work");// this part will execute else part because the statement if is false
        }














    }
}
