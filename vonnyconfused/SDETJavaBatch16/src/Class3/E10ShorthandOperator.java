package Class3;

public class E10ShorthandOperator {

    public static void main(String[] args) {

        int num =10;
        num+=90;// it means 10 add 90 =100
        num-=50;// it means 100 -50 =>50
        num*=2; // 50*2=>100
        num/=10; // 100/10 = 10

        System.out.println(num);





    }
}
