package Homework1;

public class PracticeVariable {

    public static void main(String[] args) {

        byte myVar1 = 100;
        System.out.println(myVar1);

        short myVar2 = -2000;
        System.out.println(myVar2);

        int myVar3 = 550;
        System.out.println(myVar3);

        long myVar4 = 200_675_000_000_000_000L;
        System.out.println(myVar4);

        float myVar5 = -7.5F;
        System.out.println(myVar5);

        double myVar6 = 10.5;
        System.out.println(myVar6);

        char myVar7 = 'V';
        System.out.println(myVar7);

        boolean myVar8 = true;
        System.out.println(myVar8);






    }
}
